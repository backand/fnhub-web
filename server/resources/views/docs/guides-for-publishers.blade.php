<p>Publishing a function on FnHub.io is quick and easy! Just follow these three steps to release your package to the world:</p>

<ol>
<li><p>Initialize your function package using fnhub init:</p>

<p><code>fnhub init --name myAwesomePackage --author test@example.com</code></p></li>
<li><p>Add your serverless functions to the module using fnhub add:</p>

<p><code>fnhub add -fn functionName -fnh index.handler -fnr nodejs6.10</code></p></li>
<li><p>Publish your module to fnhub.io using fnhub publish:</p>

<p><code>fnhub publish</code></p></li>
</ol>

<p>And that&#39;s it! With these three commands completed, your function is now available on FnHub.io, ready to be distributed to the serverless development community!</p>
