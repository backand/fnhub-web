<p>Looking to accelerate your serverless development with a FnHub.io package? Pulling down and deploying a function is quick and painless! Just follow these easy steps:</p>

<ol>
<li><p>Initialize your serverless function platform connection using fnhub init:</p>

<p><code>fnhub init --name myAwesomeFunctionPackage --author test@example.com</code></p></li>
<li><p>Pull down a fnhub function using fnhub include:</p>

<p><code>fnhub include reallyUsefulFunctionPackage</code></p></li>
<li><p>Make any changes necessary to configuration, environment variables, and other application-specific data.
Deploy your function to your serverless platform of choice using fnhub deploy:</p>

<p><code>fnhub deploy</code></p></li>
</ol>

<p>And that&#39;s it! FnHub.io is designed to be easy to use, both for sharing and for pulling down shared code. With these simple steps, you can save time and effort by leveraging the work of others.</p>
